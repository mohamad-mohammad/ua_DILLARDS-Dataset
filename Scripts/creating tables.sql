USE ua_DILLARDS;
CREATE TABLE DEPTINFO(
    DEPT INT NOT NULL,
    DEPTDESC CHAR(8),
    PRIMARY KEY(DEPT));


CREATE TABLE SKUINFO
     (
    SKU INTEGER NOT NULL,
    DEPT INT NOT NULL,
    CLASSID CHAR(3),
    UPC CHAR(15),
    STYLE CHAR(12),
    COLOR CHAR(12),
    SIZE CHAR(10),
    PACKSIZE INTEGER,
    VENDOR CHAR(7),
    BRAND CHAR(9),
    FOREIGN KEY (DEPT) REFERENCES DEPTINFO(DEPT),
    PRIMARY KEY (SKU));



CREATE TABLE strinfo 
     (
    STORE INTEGER NOT NULL,
    CITY CHAR(20),
    STATE CHAR(2),
    ZIP CHAR(5),
    PRIMARY KEY (STORE))
;


CREATE TABLE SKSTINFO 
  
     (
    SKU INT NOT NULL,
    STORE INTEGER NOT NULL,
    COST DECIMAL(7,2),
    RETAIL DECIMAL(7,2),
    FOREIGN KEY ( SKU) REFERENCES SKUINFO(SKU), FOREIGN KEY (STORE) REFERENCES STRINFO(STORE));



CREATE TABLE STORE_MSA
     (
    STORE INTEGER NOT NULL,
    CITY CHAR(20),
    STATE CHAR(2),
    ZIP CHAR(5),
    MSA INTEGER,
    MSA_NAME CHAR(40),
    MSA_POP INTEGER,
    CENSUS_REGION CHAR(30),
    LATITUDE FLOAT,
    LONGITUDE FLOAT,
    MSA_INCOME INTEGER,
    MSA_HIGH DECIMAL(3,1),
    MSA_BACH DECIMAL(3,1),
PRIMARY KEY ( STORE ));


CREATE TABLE TRNSACT
     (
    SKU INTEGER NOT NULL,
    STORE INTEGER NOT NULL,
    REGISTER INTEGER NOT NULL,
    TRANNUM CHAR(5) NOT NULL,
    INTERID CHAR(9),
    SALEDATE DATE NOT NULL,
    STYPE CHAR(1),
    QUANTITY INTEGER,
    ORGPRICE DECIMAL(6,2),
    SPRICE DECIMAL(6,2),
    AMT DECIMAL(7,2),
    SEQ CHAR(9) NOT NULL,
    MIC CHAR(15),
    FOREIGN KEY (SKU) REFERENCES SKUINFO(SKU),
    FOREIGN KEY (STORE) REFERENCES STRINFO(STORE),
    PRIMARY KEY ( SKU ,STORE ,REGISTER ,TRANNUM ,SALEDATE ,SEQ ));
